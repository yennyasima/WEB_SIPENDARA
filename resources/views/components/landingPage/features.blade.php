                <a class="text-center p-2 bg-[#D95639] rounded text-white"
                    href=${gambar}
                    download='data ${title} terbaru'>Download Gambar</a>
            </div>
        `
        containerData.innerHTML += data
        containerData.classList.add('flex')
        containerData.classList.add('space-x-2')
        popUpData.append(containerData)
    }

    buttonDbd.addEventListener('click', function() {
        title.innerHTML = 'Data DBD'
        containerData.innerHTML = ''
        popUp.classList.remove('hidden')
        coverPopUp.classList.remove('hidden')
        @if ($penyakitDbd && $penyakitDbd->gambar)
            renderDetailParam("{{ asset('storage/dataPenyakitDbd/' . $penyakitDbd->gambar) }}", 'Penyakit DBD');
        @else
            console.error("Peta Penyakit DBD Tidak Tersedia");
        @endif
    })

    buttonkpdtpddk.addEventListener('click', function() {
        title.innerHTML = 'Data Kepadatan Penduduk'
        containerData.innerHTML = ''
        popUp.classList.remove('hidden')
        coverPopUp.classList.remove('hidden')
        @if ($kepadatanDbd && $kepadatanDbd->gambar)
            renderDetailParam("{{ asset('storage/kepadatanPendudukDbd/' . $kepadatanDbd->gambar) }}",
                'Kepadatan Penduduk');
        @else
            console.error("Peta Kepadatan Penduduk Tidak Tersedia");
        @endif
    })

    buttonfakling.addEventListener('click', function() {
        title.innerHTML = 'Data Iklim'
        containerData.innerHTML = ''
        popUp.classList.remove('hidden')
        coverPopUp.classList.remove('hidden')
        @if ($faktorDbd && $faktorDbd->gambar)
            renderDetailParam("{{ asset('storage/faktorLingkunganDbd/' . $faktorDbd->gambar) }}",
                'Faktor Lingkungan');
        @else
            console.error("Peta Iklim Tidak Tersedia");
        @endif
    })

